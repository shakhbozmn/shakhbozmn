// Run offline (Node), NOT inside the Cloudflare Worker — @napi-rs/canvas
// needs native bindings that don't exist in the workerd runtime. This script
// pre-renders every glyph the profile actually uses into a small coverage
// bitmap (0-255 alpha per pixel), then serializes the result into a plain
// JS module the Worker can read with zero dependencies at request time.
//
// Usage:
//   npm install @napi-rs/canvas
//   node build-font-atlas.mjs
//
// Re-run this any time you add a new character to status.js / live.js /
// previewGif.js — it will tell you if anything is still missing.

import { createCanvas, GlobalFonts } from "@napi-rs/canvas";
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const FONT_PATH = path.join(__dirname, "..", "fonts", "JetBrainsMono-Regular.ttf");
const FONT_FAMILY = "JetBrains Mono";
GlobalFonts.registerFromPath(FONT_PATH, FONT_FAMILY);

// Render size. Bigger = crisper glyphs but a bigger generated file.
// 16px is plenty for the small terminal-window GIF this feeds.
const RENDER_SIZE = 16;

// Every character actually used across font.js / status.js / live.js /
// previewGif.js, PLUS full printable ASCII so nothing new silently breaks.
// (This is exactly the "missing glyph" bug: `"` and en-dash `–` were used
// in status.js but never added here, so they rendered as a fallback box.)
const ASCII_RANGE = [];
for (let code = 0x20; code <= 0x7e; code++) ASCII_RANGE.push(String.fromCodePoint(code));

const EXTRA_CHARS = [
  "·", "—", "–", "•", "→", "↳", "▌", "▍", "×", "≤", "≥", "…",
  "\u2018", "\u2019", "\u201C", "\u201D", // curly quotes, just in case
];

const CHARSET = Array.from(new Set([...ASCII_RANGE, ...EXTRA_CHARS]));

// Use a monospace canvas to measure the font's true fixed advance width,
// then render every glyph into a cell of that exact size so spacing is
// perfectly even (real terminal fonts are monospace by design).
const measureCanvas = createCanvas(200, 200);
const mctx = measureCanvas.getContext("2d");
mctx.font = `${RENDER_SIZE}px "${FONT_FAMILY}"`;
const CELL_WIDTH = Math.ceil(mctx.measureText("M").width);
const ASCENT = Math.ceil(RENDER_SIZE * 1.05);
const DESCENT = Math.ceil(RENDER_SIZE * 0.35);
const CELL_HEIGHT = ASCENT + DESCENT;

const glyphs = {};
const missing = [];

for (const ch of CHARSET) {
  const canvas = createCanvas(CELL_WIDTH, CELL_HEIGHT);
  const ctx = canvas.getContext("2d");
  ctx.clearRect(0, 0, CELL_WIDTH, CELL_HEIGHT);
  ctx.font = `${RENDER_SIZE}px "${FONT_FAMILY}"`;
  ctx.textBaseline = "alphabetic";
  ctx.fillStyle = "#fff";
  ctx.fillText(ch, 0, ASCENT);

  const { data } = ctx.getImageData(0, 0, CELL_WIDTH, CELL_HEIGHT);
  const rows = [];
  let hasInk = ch === " "; // space is intentionally blank
  for (let y = 0; y < CELL_HEIGHT; y++) {
    let row = "";
    for (let x = 0; x < CELL_WIDTH; x++) {
      const alpha = data[(y * CELL_WIDTH + x) * 4 + 3];
      if (alpha > 8) hasInk = true;
      // Pack alpha as a 2-hex-digit string per pixel -> compact + simple to decode.
      row += alpha.toString(16).padStart(2, "0");
    }
    rows.push(row);
  }

  if (!hasInk) missing.push(ch);
  glyphs[ch] = rows.join("");
}

if (missing.length) {
  console.warn(
    "WARNING: these characters rendered as blank (font may not have a glyph for them):",
    missing,
  );
}

const out = `// AUTO-GENERATED by scripts/build-font-atlas.mjs — do not hand-edit.
// Source font: JetBrains Mono Regular (SIL OFL 1.1), rendered at ${RENDER_SIZE}px.
// Each glyph is a ${CELL_WIDTH}x${CELL_HEIGHT} coverage map: one 2-hex-digit
// alpha value (00-ff) per pixel, row-major, concatenated into one string.

export const CELL_WIDTH = ${CELL_WIDTH};
export const CELL_HEIGHT = ${CELL_HEIGHT};

export const GLYPHS = ${JSON.stringify(glyphs)};
`;

writeFileSync(path.join(__dirname, "..", "src", "font-atlas.generated.js"), out);
console.log(
  `Wrote font-atlas.generated.js — ${CHARSET.length} glyphs, cell ${CELL_WIDTH}x${CELL_HEIGHT}px.`,
);
