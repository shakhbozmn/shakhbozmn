const FALLBACK_GLYPH = [0x1F, 0x01, 0x01, 0x01, 0x01, 0x01, 0x1F];

export function drawText(ctx, text, x, y, color) {
  let cursor = x;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    if (ch === "\n") {
      cursor = x;
      y += CELL_HEIGHT;
      continue;
    }
    const glyph = GLYPHS[ch] || FALLBACK_GLYPH;
    for (let row = 0; row < GLYPH_HEIGHT; row++) {
      const bits = glyph[row];
      for (let col = 0; col < GLYPH_WIDTH; col++) {
        if (bits & (1 << (GLYPH_WIDTH - 1 - col))) {
          ctx.fillRect(cursor + col, y + row, 1, 1, color);
        }
      }
    }
    cursor += CELL_WIDTH;
  }
}

export function textWidth(text) {
  return text.length * CELL_WIDTH;
}
